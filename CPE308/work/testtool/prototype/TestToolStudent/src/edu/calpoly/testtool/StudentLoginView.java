    private void loginButtonClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonClicked
        if (!usernameField.getText().equals("") && !passwordField.getText().equals("")) {
            SingleFrameApplication app = (SingleFrameApplication)this.getApplication();
            app.show(new StudentHomeView(app));
            app.hide(this);
        }
        else {
           SingleFrameApplication app = (SingleFrameApplication)this.getApplication();
           app.show(new LoginError(app.getMainFrame(), false));
        }
    }//GEN-LAST:event_loginButtonClicked

